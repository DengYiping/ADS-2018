import matplotlib.pyplot as plt

def breakTuples(tuples):
  xs = []
  ys = []
  for x, y in tuples:
    xs.append(x)
    ys.append(y)
  return xs, ys

def plotTogether(tuples):
  xs, ys = breakTuples(tuples)
  plt.plot(xs, ys)

